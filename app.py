@app.route('/api/register', methods=['POST'])
def register():
    data = request.json or {}
    user_id = data.get('user_id')
    if not user_id:
        return jsonify({"error": "Missing user_id"}), 400
    
    user_id = int(user_id)
    if user_id not in user_database:
        user_database[user_id] = 15
        logger.info(f"✅ New user {user_id} registered with 15 points")
    
    return jsonify({"user_id": user_id, "points": user_database[user_id]}), 200

@app.route('/api/user/<int:user_id>', methods=['GET'])
def get_user(user_id):
    points = user_database.get(user_id, 0)
    return jsonify({"user_id": user_id, "points": points}), 200

@app.route('/api/earn', methods=['POST'])
def earn():
    data = request.json or {}
    user_id = data.get('user_id')
    points_to_add = data.get('points', 10)
    
    if not user_id:
        return jsonify({"error": "Missing user_id"}), 400
        
    user_id = int(user_id)
    if user_id not in user_database:
        user_database[user_id] = 15
        
    user_database[user_id] += points_to_add
    logger.info(f"🎁 User {user_id} earned {points_to_add} points. Total: {user_database[user_id]}")
    
    return jsonify({"success": True, "points": user_database[user_id]}), 200

@app.route('/api/shop/buy', methods=['POST'])
def create_invoice():
    data = request.json or {}
    user_id = data.get('user_id')
    pkg_id = data.get('package')
    
    if not user_id or pkg_id not in PACKAGES:
        return jsonify({"success": False, "error": "Invalid request"}), 400
        
    pkg = PACKAGES[pkg_id]
    
    url = f"https://api.telegram.org/bot{TOKEN}/createInvoiceLink"
    payload = {
        "title": f"Omen {pkg_id.title()} Points",
        "description": f"Αγορά {pkg['points']} πόντων για ανάλυση καφεμαντείας.",
        "payload": f"{user_id}_{pkg_id}",
        "provider_token": "",  
        "currency": "XTR",    
        "prices": pkg["prices"]
    }
    
    try:
        response = requests.post(url, json=payload)
        res_data = response.json()
        if res_data.get("ok"):
            return jsonify({"success": True, "invoice_link": res_data["result"]})
        else:
            logger.error(f"Telegram API Error: {res_data}")
            return jsonify({"success": False, "error": "Αποτυχία δημιουργίας συνδέσμου πληρωμής"})
    except Exception as e:
        logger.error(f"Invoice creation failed: {e}")
        return jsonify({"success": False, "error": str(e)}), 500

