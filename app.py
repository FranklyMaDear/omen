DB_FILE = "omen_database.db"

def init_db():
    """Αρχικοποίηση της βάσης δεδομένων και δημιουργία του πίνακα χρηστών αν δεν υπάρχει."""
    conn = sqlite3.connect(DB_FILE)
    cursor = conn.cursor()
    cursor.execute('''
        CREATE TABLE IF NOT EXISTS users (
            user_id INTEGER PRIMARY KEY,
            points INTEGER DEFAULT 15
        )
    ''')
    conn.commit()
    conn.close()
    logger.info("✅ SQLite Database initialized and checked.")

def get_user_points(user_id):
    """Επιστρέφει τους πόντους του χρήστη. Αν δεν υπάρχει, τον δημιουργεί με 15 πόντους."""
    conn = sqlite3.connect(DB_FILE)
    cursor = conn.cursor()
    cursor.execute("SELECT points FROM users WHERE user_id = ?", (user_id,))
    row = cursor.fetchone()
    if row is None:
        cursor.execute("INSERT INTO users (user_id, points) VALUES (?, 15)", (user_id,))
        conn.commit()
        points = 15
    else:
        points = row[0]
    conn.close()
    return points

def update_user_points(user_id, points_to_add):
    """Προσθέτει ή αφαιρεί πόντους από τον χρήστη διασφαλίζοντας ότι δεν πέφτουν κάτω από το 0."""
    conn = sqlite3.connect(DB_FILE)
    cursor = conn.cursor()
    cursor.execute("SELECT points FROM users WHERE user_id = ?", (user_id,))
    row = cursor.fetchone()
    if row is None:
        current_points = 15
        new_points = max(0, current_points + points_to_add)
        cursor.execute("INSERT INTO users (user_id, points) VALUES (?, ?)", (user_id, new_points))
    else:
        current_points = row[0]
        new_points = max(0, current_points + points_to_add)
        cursor.execute("UPDATE users SET points = ? WHERE user_id = ?", (new_points, user_id))
    conn.commit()
    conn.close()
    return new_points

